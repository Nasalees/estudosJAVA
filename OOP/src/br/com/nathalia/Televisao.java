package br.com.nathalia;

public class Televisao {
	private int canal;
	private int volume;
	

	public int getCanal() {
		return this.canal;
	}
	
	public void setCanal(int canais) {
		this.canal = canais;
	}
	
	public int getVolume() {
		return this.volume;
	}
	
	public void setVolume(int volume) {
		this.volume = volume;
	}
}
